let CURRENT_LANG = "en";
function setLang(lang) { CURRENT_LANG = (lang==="es")?"es":"en"; }
function t(key) { const d=I18N[CURRENT_LANG]||I18N.en; return d[key]!==undefined?d[key]:(I18N.en[key]||key); }
function applyTranslations(root=document) {
  root.querySelectorAll("[data-i18n]").forEach(el=>el.textContent=t(el.getAttribute("data-i18n")));
  root.querySelectorAll("[data-i18n-ph]").forEach(el=>el.setAttribute("placeholder",t(el.getAttribute("data-i18n-ph"))));
  document.documentElement.lang = CURRENT_LANG;
}
window.I18N_API = { setLang, t, applyTranslations, getLang:()=>CURRENT_LANG };
